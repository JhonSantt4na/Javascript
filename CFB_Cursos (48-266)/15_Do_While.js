// Execulta pelomenos uma vez o bloco de comando, antes da expressão condicional
console.log("Usando o while")
let n = 10
while (n < 10) {
    console.log("Jhonn")
    n++
}
console.log("Fim do Programa")

// Usando o  do while
console.log("Usando o Do while")
do {
    console.log("Jhonn")
    n++
} while (n < 10);
console.log("Fim do Programa")
// ou seja é garantido operar uma vez o bloco de comando