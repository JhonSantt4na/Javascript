var n1 = 1;
var n2 = 2;

// Operadores Relacionais               (Sempre Retorna Verdade ou Falso)

console.log(n1 > n2);  // false             (n1 maior n2)
console.log(n1 >= n2); // false             (n1 maior igual n2)
console.log(n1 < n2);  // true              (n1 menor n2)
console.log(n1 <= n2); // true              (n1 menor igual n2)

// Operadores de Comparação 
var n1 = 2     // tipo numero
var n2 = "2"   // tipo string 
var n3 = 3     // tipo numero 

console.log(n1 == n2)    // true              (n1 igual n2)
console.log(n1 === n2)   // false             (n1 Totalmenteigual n2)
console.log(n1 != n3)    // true              (n1 diferente n3)
console.log(!(n1 == n3)) // true    (inverte) (not n1 igual n3)

// === se refere ao valor,tipo e possição de memoria para ser true